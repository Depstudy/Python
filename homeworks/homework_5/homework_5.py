rus_words = {'One': 'Один', 'Two': 'Два', 'Three': 'Три', 'Four': 'Четыре'}
new_file = []
with open('task_4.txt', 'r') as my_f:
    for line in my_f:
        line = line.split(' ', 1)
        new_file.append(rus_words[line[0]] + ' ' + line[1])
with open('task_4_new.txt', 'w') as my_f:
    my_f.writelines(new_file)
