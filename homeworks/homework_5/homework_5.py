#Task 3:
# Создать текстовый файл (не программно),
# построчно записать фамилии сотрудников и величину их окладов (не менее 10 строк).
# Определить, кто из сотрудников имеет оклад менее 20 тыс., вывести фамилии этих сотрудников.
# Выполнить подсчет средней величины дохода сотрудников.
# Пример файла:
# Иванов 23543.12
# Петров 13749.32

with open('task_3.txt') as my_f:
    fix = 20000
    sal = []
    low_sal = []
    my_list = my_f.read().split('\n')
    for i in my_list:
        i = i.split()
        if float(i[1]) < fix:
            low_sal.append(i[0])
        sal.append(i[1])
print(f'Сотрудники имеющие оклад ниже 20000: {low_sal}')
print(f'Средняя величина дохода сотрудников {round(sum(map(float, sal)) / len(sal), 2)}')
